    public void llenarTabla() {
        ConsumoAPI consumo = new ConsumoAPI();
        String respuesta = consumo.consumoGET("http://localhost/APIenPHP_JAVA/Obtener.php");

        tableModel = (DefaultTableModel) tablaUsuarios.getModel();
        tableModel.setNumRows(0);

        if (respuesta != null) {
            JsonParser parser = new JsonParser();
            JsonObject object = parser.parse(respuesta).getAsJsonObject();

            JsonArray registros = object.getAsJsonArray("registros");

            for (int i = 0; i < registros.size(); i++) {
                JsonObject registro = registros.get(i).getAsJsonObject();
                String cedula = registro.get("cedula").getAsString();
                String nombre = registro.get("nombres").getAsString();
                String apellido = registro.get("apellidos").getAsString();
                String telefono = registro.get("telefono").getAsString();

                Object[] temp = new Object[]{cedula, nombre, apellido, telefono};
                tableModel.addRow(temp);
            }
        }

    }


