    private void buscarParqueadero() {
        

        String placa = campo_placa.getText();

        Map<String, String> dataPost = new HashMap<>();
        dataPost.put("placa", placa);
        try {
            String response = conexion.consumoPOST("http://localhost/apiparqueadero/vehiculos/findVehiculo.php", dataPost);
      
            JsonObject jsonObject = gson.fromJson(response, JsonObject.class);
            

            
            JsonArray registros = jsonObject.getAsJsonArray("registros");
            System.out.println(registros);
            //!registros.isEmpty()
            //registros.size() > 0
            if (registros.size() > 0) {

                tableModel = (DefaultTableModel) tabla_parqueaderos.getModel();
                tableModel.setNumRows(0);
                
                for (JsonElement registro : registros) {
                    JsonObject registrosOb = registro.getAsJsonObject();

                    String placaObtenida = registrosOb.get("placa").getAsString();
                    String propietario = registrosOb.get("propietario").getAsString();
                    String tipo = registrosOb.get("tipo").getAsString();

                    Object[] temporal = new Object[]{placaObtenida, propietario, tipo};
                    tableModel.addRow(temporal);

                    centrarTextTabla();

                }

                
            }else{
                JOptionPane.showMessageDialog(null, "No se encontraron resultados");
                llenarTabla();
            }
        } catch (Exception e) {
            System.out.println("error al obtener el vehiculo"+ e);
        }
        campo_placa.setText("");
    }

    private void llenarTabla() {
        try {
            String response = conexion.consumoGET("http://localhost/apiparqueadero/vehiculos/getVehiculos.php");
            JsonObject jsonObject = gson.fromJson(response, JsonObject.class);

            JsonArray registros = jsonObject.getAsJsonArray("registros");
            //!registros.isEmpty()
            //registros.size() > 0
            if (registros.size() > 0) {
                tableModel = (DefaultTableModel) tabla_parqueaderos.getModel();
                tableModel.setNumRows(0);

                for (JsonElement registro : registros) {
                    JsonObject registrosOb = registro.getAsJsonObject();

                    String placaObtenida = registrosOb.get("placa").getAsString();
                    String propietario = registrosOb.get("propietario").getAsString();
                    String tipo = registrosOb.get("tipo").getAsString();

                    Object[] temporal = new Object[]{placaObtenida, propietario, tipo};
                    tableModel.addRow(temporal);

                    centrarTextTabla();

                }
            } else {

            }

        } catch (Exception e) {
            System.out.println("error al obtener el parqueadero");
        }
    }

    private void centrarTextTabla() {
        JTableHeader header = tabla_parqueaderos.getTableHeader();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        for (int i = 0; i < tabla_parqueaderos.getColumnCount(); i++) {
            tabla_parqueaderos.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
        tabla_parqueaderos.repaint();
    }
